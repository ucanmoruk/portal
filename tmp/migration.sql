-- NKR_Formul: Ürün formülü bileşenleri
IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME='NKR_Formul')
BEGIN
    CREATE TABLE NKR_Formul (
        ID         INT           IDENTITY(1,1) PRIMARY KEY,
        NKRID      INT           NOT NULL,
        HammaddeID INT           NULL,
        INCIName   NVARCHAR(500) NULL,
        Miktar     FLOAT         NULL,
        DaP        FLOAT         NULL,
        Noael      FLOAT         NULL
    );
END

-- NKR_Log: Ürün geçmişi
IF NOT EXISTS (SELECT 1 FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME='NKR_Log')
BEGIN
    CREATE TABLE NKR_Log (
        ID          INT           IDENTITY(1,1) PRIMARY KEY,
        NKRID       INT           NOT NULL,
        Tarih       DATETIME      NOT NULL DEFAULT GETDATE(),
        KullaniciID INT           NULL,
        Eylem       NVARCHAR(100) NULL,
        Aciklama    NVARCHAR(MAX) NULL
    );
END
